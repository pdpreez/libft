#include "libft.h"

size_t	ft_wordcount(char const *s)
{
	size_t	i;
	size_t	wc;

	i = 0;
	wc = 0;
	while (s[i])
	{
		if ((ft_isspace(s[i + 1]) == 1 || s[i + 1] == '\0') && (!(ft_isspace(s[i]))))
			wc++;
		i++;
	}
	return (wc);
}
